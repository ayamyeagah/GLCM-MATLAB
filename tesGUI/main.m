function pushbutton2_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton2 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
global filename

a = imread(filename);
imageArray = rgb2gray(a);
imageArray = imsharpen(imageArray,'Radius',10,'Amount',2);
imageArray = imresize(imageArray,[224 224]);
b=imageArray;
axes(handles.axes1);
imshow(a);
title('RGB');
grid on;

axes(handles.axes2);
imshow(b);
title('Greyscale');
grid on;

c = graycoprops(b);

c1 = c.Contrast;
c2 = c.Correlation;
c3 = c.Energy;
c4 = c.Homogeneity;

set(handles.edit2,'string',c1);
set(handles.edit3,'string',c2);
set(handles.edit4,'string',c3);
set(handles.edit5,'string',c4);

proses = [c1 c2 c3 c4];

load model

tes = proses';
yp=(sim(netWr,tes))';
hasil = yp;

set(handles.edit6,'string',yp(1,1));
set(handles.edit7,'string',yp(1,2));
set(handles.edit8,'string',yp(1,3));
set(handles.edit9,'string',yp(1,4));

maksimum = max(hasil);

if maksimum == yp(1,1)
set(handles.result,'string','AGARICUS');
%[y fs] = audioread('good.mp3');
%sound(y,fs);

elseif maksimum == yp(1,2)
set(handles.result,'string','BOLETUS');
%[y fs] = audioread('overspatter.mp3');
%sound(y,fs);

elseif maksimum == yp(1,3)
set(handles.result,'string','ENTOLOMA');
%[y fs] = audioread('porosity.mp3');
%sound(y,fs);

elseif maksimum == yp(1,4)
set(handles.result,'string','RUSSULA');
%[y fs] = audioread('undercut.mp3');
%sound(y,fs);

end



